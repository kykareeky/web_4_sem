                    <strong>Как перекодировать строку в PHP из одной кодировки в другую?</strong>
                    <div class="answer">
                        <p>В PHP для перекодировки строк используется функция <code>iconv()</code>:</p>
                        <pre>iconv(string $in_charset, string $out_charset, string $str)</pre>
                        <p><strong>Параметры:</strong></p>
                        <ul>
                            <li><code>$in_charset</code> - исходная кодировка</li>
                            <li><code>$out_charset</code> - целевая кодировка</li>
                            <li><code>$str</code> - перекодируемая строка</li>
                        </ul>
                        <p><strong>Примеры из лабораторной работы:</strong></p>
                        <pre>// перекодировка из UTF-8 в CP1251 для анализа
$text_for_analysis = iconv("utf-8", "cp1251//IGNORE", $original_text);

// перекодировка обратно из CP1251 в UTF-8 для вывода
$word_utf8 = iconv("cp1251", "utf-8//IGNORE", $word_lower);</pre>
                        <p>Модификатор <code>//IGNORE</code> игнорирует символы, которые нельзя перекодировать. Также можно использовать <code>//TRANSLIT</code> для транслитерации.</p>
                        <p>Альтернативные функции: <code>mb_convert_encoding()</code> (если доступна multibyte-библиотека).</p>
