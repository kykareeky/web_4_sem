                        <ul>
                            <li><code>strtolower($str)</code> - преобразует строку в нижний регистр</li>
                            <li><code>strtoupper($str)</code> - преобразует строку в верхний регистр</li>
                            <li><code>ucfirst($str)</code> - преобразует первый символ строки в верхний регистр</li>
