                    <strong>Что такое кодировка?</strong>
                    <div class="answer">
                        <p><strong>Кодировка</strong> (кодировка символов) - это система правил, которая ставит в соответствие каждому символу (букве, цифре, знаку препинания) определенный числовой код (обычно последовательность байт) для хранения и передачи в компьютерных системах.</p>
                        <p>Компьютеры работают с числами, поэтому для представления текста необходимо преобразовать символы в числа. Кодировка определяет, как именно это преобразование выполняется.</p>
                        <p>Без правильной кодировки текст будет отображаться некорректно - вместо букв будут видны "кракозябры".</p>
