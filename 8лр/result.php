                    <div class="answer">
                        <p>Multibyte-функции (с префиксом <code>mb_</code>) предназначены для корректной работы с многобайтовыми кодировками, такими как UTF-8, где один символ может занимать несколько байт.</p>
                        <p><strong>Отличия от стандартных функций:</strong></p>
                        <ul>
                            <li>Стандартные функции (<code>strlen()</code>, <code>strtolower()</code> и др.) работают на уровне байт - они считают, что каждый символ = 1 байт</li>
                            <li>Multibyte-функции работают на уровне символов - они учитывают реальную длину символа в байтах</li>
                            <li>Например, <code>strlen("привет")</code> в UTF-8 вернет 12 (6 букв × 2 байта), а <code>mb_strlen("привет")</code> вернет 6</li>
