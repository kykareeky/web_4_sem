/**
 * Функция преобразует число в ссылку на соответствующую таблицу умножения.
 * Если число от 2 до 9 - это ссылка на столбец.
 * Числа, не попадающие в этот диапазон (например, произведения >9), выводятся как есть.
 * 
 * @param int $x число для преобразования
 * @return string HTML-код ссылки или просто число
 */
function outNumAsLink($x) {
    // учитываем, что ссылки должны быть только на цифры (условие: "все цифры (и только цифры)")
    // но по заданию ссылки должны быть на 2,3,4,5,6,7,8,9 и на результат произведения
    // результат может быть двузначным - тогда ссылка не ставится
    if ($x >= 2 && $x <= 9) {
        // возвращаем ссылку, которая сбрасывает тип верстки (html_type не указываем)
        return '<a href="?content=' . $x . '">' . $x . '</a>';
    } else {
        return (string)$x; // просто число как строка
    }
}

/**
 * Функция выводит столбец таблицы умножения для заданного числа.
 * Использует outNumAsLink для формирования ссылок.
 * 
 * @param int $n число, для которого строится столбец (2..9)
 */
function outRow($n) {
    for ($i = 2; $i <= 9; $i++) {
        // формируем строку вида: 2x3=6, где каждое число - ссылка (если применимо)
        echo outNumAsLink($n) . 'x' . outNumAsLink($i) . '=' . outNumAsLink($i * $n) . '<br>';
    }
}

